package com.example.model;

import java.util.Date;


public class Paragoni {
	
	private String masa;
	private String zat_promet;
	private String vraboten;
	private Date datum;
	private Boolean ispratena;
	private Integer id;
	private Integer broj;
	
	
	public String getMasa() {
		return masa;
	}
	public void setMasa(String masa) {
		this.masa = masa;
	}
	public String getZat_promet() {
		return zat_promet;
	}
	public void setZat_promet(String zat_promet) {
		this.zat_promet = zat_promet;
	}
	public String getVraboten() {
		return vraboten;
	}
	public void setVraboten(String vraboten) {
		this.vraboten = vraboten;
	}
	public Date getDatum() {
		return datum;
	}
	public void setDatum(Date datum) {
		this.datum = datum;
	}
	public Boolean getIspratena() {
		return ispratena;
	}
	public void setIspratena(Boolean ispratena) {
		this.ispratena = ispratena;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getBroj() {
		return broj;
	}
	public void setBroj(Integer broj) {
		this.broj = broj;
	}
	
	
}
